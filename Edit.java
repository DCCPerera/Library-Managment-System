     public void DeleteEntry(String file,String removeTerm)
    {
        try {
            File inputFile = new File(file);
            File tempFile = new File("myTempFile.txt");

            BufferedReader reader = new BufferedReader(new FileReader(inputFile));
            BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));
            String lineToRemove = removeTerm;
           // System.out.println(removeTerm);
            String currentLine;

            while ((currentLine = reader.readLine()) != null) {
                String trimmedLine = currentLine.trim();
                if (trimmedLine.contains(lineToRemove)) continue;
                writer.write(currentLine + System.getProperty("line.separator"));
            }

            writer.close();
            reader.close();
            boolean del=inputFile.delete();
           // System.out.println(del);
            boolean successful=tempFile.renameTo(inputFile);
        }
        catch (IOException e)
        {
            JOptionPane.showMessageDialog(null,"An error occurred.");
            e.printStackTrace();
        }
    }
     

    
        public void reWriteRecordSerial()
    {
        try{
            File file1=new File("book.txt");
            FileWriter fileWriter=new FileWriter(file1,true);
            fileWriter.write(newserialNumber+"\t"+changedTitle+"\t"+changedAuthor+"\t"+changedGenre+"\t"+placement+"\t"+changedborrowed+"\t"+changedBorrower+"\t"+changedDate);
            fileWriter.close();}
        catch (IOException e)
        {
            JOptionPane.showMessageDialog(null,"An error occurred.");
            e.printStackTrace();
        }
    }
    

