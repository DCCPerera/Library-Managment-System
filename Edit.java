    public void AssigningTheBookRecords()
    {
        try {
            y = new Scanner(new File("book.txt"));
        }
        catch (FileNotFoundException e)
        {
            JOptionPane.showMessageDialog(null,"An error occurred");
        }

        while (y.hasNext()) {
            serialNumber = y.next();
            title=y.next();
            author = y.next();
            genre=y.next();
            placement=y.next();
            borrowed=y.nextInt();
            borrower=y.next();
            dueDate=y.next();

            if ( serialNumber.equals(SearchSerial))
            {
                changedSerial=serialNumber;
                changedTitle=title;
                changedAuthor=author;
                changedGenre=genre;
                changedPlacement=placement;
                changedborrowed=borrowed;
                changedBorrower=borrower;
                changedDate=dueDate;
                break;
            }
        }
        

    }


